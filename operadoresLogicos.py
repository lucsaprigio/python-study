# Operadores Lógicos
idade = 15
print(idade > 17) # Maior que
print(idade < 17) # Menor que
print(idade <= 17) # Menor ou igual que
print(idade >= 17) # Maior ou igual que
print(idade == 17) # igual
print(idade != 17) # Diferente de
# Comparações entre outros tupos
print(True == True)
print('Rafael' == 'rafael') 

""" Python é Case sensitive, então ele compara letrar minúsculas e maiúsculas, faz diferença! """